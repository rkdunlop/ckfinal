public class Inventory 
{
    public static int water;
    public static int food;
   
    public Inventory(int water, int food)
    {
        this.water = water;
        this.food = food;
    }
    
    public int getWater()
    {
        return this.water;
    }
    
    public int getFood()
    {
        return this.food;
    }
}
    
